package com.gzt.proje;

public class Product {
    String image;
    String title;
    String price;
    String seller;
    String link;

    public Product(String image, String title, String price, String seller, String link) {
        this.image = image;
        this.title = title;
        this.price = price;
        this.seller = seller;
        this.link = link;
    }
}
